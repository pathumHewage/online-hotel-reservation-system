package com.customer;

	public class room {
	    private int roomid;
	    private String roomname;
	    private String type;
	    private String roomnumber;
	    private String pets;
	    private String serve;
	    private String menu;
	    private String gymFacility;
	    private String pswimmingpol;
	    private String sorrounding;
	    private String recommedation;
	    private String discountSeason;
	    private String guide;
	    private String vehicleFacility;
	  
	    
	   
	    	
		 


		public room(int roomid, String roomname, String type, String roomnumber, String pets, String serve, String menu,
				String gymFacility, String pswimmingpol, String sorrounding, String recommedation,
				String discountSeason, String guide, String vehicleFacility) {
			super();
			this.roomid = roomid;
			this.roomname = roomname;
			this.type = type;
			this.roomnumber = roomnumber;
			this.pets = pets;
			this.serve = serve;
			this.menu = menu;
			this.gymFacility = gymFacility;
			this.pswimmingpol = pswimmingpol;
			this.sorrounding = sorrounding;
			this.recommedation = recommedation;
			this.discountSeason = discountSeason;
			this.guide = guide;
			this.vehicleFacility = vehicleFacility;
		}





		public int getRoomid() {
			return roomid;
		}


		


		public String getRoomname() {
			return roomname;
		}


		


		public String getType() {
			return type;
		}


		

		public String getRoomnumber() {
			return roomnumber;
		}



		public String getPets() {
			return pets;
		}
		
		public String getServe() {
			return serve;
		}


		


		public String getMenu() {
			return menu;
		}


		


		public String getGymFacility() {
			return gymFacility;
		}


		


		public String getPswimmingpol() {
			return pswimmingpol;
		}


		


		public String getSorrounding() {
			return sorrounding;
		}


		

		public String getRecommedation() {
			return recommedation;
		}


		


		public String getDiscountSeason() {
			return discountSeason;
		}




		public String getGuide() {
			return guide;
		}


		


		public String getVehicleFacility() {
			return vehicleFacility;
		}


		
	
	
	

}

